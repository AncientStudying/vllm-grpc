"""FR-005c chat-corpus parity tests.

M5.2 routes both REST and gRPC chat cohorts through a shared
``RequestSample`` corpus (ShareGPT V3 subset by default). For the same
iteration index, both protocols MUST send byte-identical engine-input
content (same prompt text, same max_tokens, same temperature).

These tests lock in the contract that a future maintainer can't silently
break by:
- changing one protocol's prompt indexing
- overriding the corpus's max_tokens / temperature on one side only
- inserting a per-protocol prompt transform that diverges engine-input

The ShareGPT corpus itself (and its provenance) is generated by
``scripts/python/gen_chat_corpus.py``; this test file does NOT depend on
the network — synthetic corpora are built inline for unit tests.
"""

from __future__ import annotations

import json

import httpx
import pytest
from vllm_grpc.v1 import chat_pb2
from vllm_grpc_bench.corpus import DEFAULT_CHAT_CORPUS_PATH, RequestSample, load_corpus
from vllm_grpc_bench.rest_cohort import run_rest_cohort

_TEST_TOKEN = "test-bearer-abcdef0123"


def _synthetic_corpus(n: int = 5) -> list[RequestSample]:
    return [
        RequestSample(
            id=f"corpus-test-{i:04d}",
            messages=[
                {"role": "user", "content": f"Sample prompt {i} from the synthetic test corpus."}
            ],
            model="Qwen/Qwen3-0.6B",
            max_tokens=128,
            temperature=0.0,
            seed=42,
            bucket="short" if i < 2 else ("medium" if i < 4 else "long"),
        )
        for i in range(n)
    ]


def test_default_chat_corpus_path_points_at_sharegpt_subset() -> None:
    """The default chat corpus is the committed ShareGPT 1k subset.
    Anchors the default-corpus expectation so a future refactor that
    moves the file fails this test by name.
    """
    assert DEFAULT_CHAT_CORPUS_PATH.name == "chat_sharegpt_1000.json"
    assert DEFAULT_CHAT_CORPUS_PATH.parent.name == "corpus"


def test_default_chat_corpus_loads_and_has_expected_size() -> None:
    """The committed ShareGPT subset has 1000 samples covering all three
    buckets. If the corpus is regenerated with different parameters, this
    test fails — forcing an explicit acknowledgement of the change."""
    samples = load_corpus(DEFAULT_CHAT_CORPUS_PATH)
    assert len(samples) == 1000
    buckets = {s.bucket for s in samples}
    assert buckets == {"short", "medium", "long"}


def test_default_chat_corpus_all_samples_have_required_fields() -> None:
    """Every committed sample carries the per-request fields the harness
    needs: messages, max_tokens, temperature, seed, bucket."""
    samples = load_corpus(DEFAULT_CHAT_CORPUS_PATH)
    for s in samples:
        assert s.id.startswith("sharegpt-")
        assert len(s.messages) == 1, "ShareGPT subset is first-turn-only"
        assert s.messages[0]["role"] == "user"
        assert isinstance(s.messages[0]["content"], str)
        assert 4 <= len(s.messages[0]["content"]) <= 2048
        assert s.max_tokens == 128, "max_tokens=128 is the vLLM benchmark convention"
        assert s.temperature == 0.0
        assert s.bucket in ("short", "medium", "long")


def test_request_sample_bucket_field_optional_for_legacy_corpora() -> None:
    """``RequestSample.bucket`` defaults to ``"unspecified"`` so older
    corpora (chat_nonstreaming.json without the field) load unchanged."""
    s = RequestSample(
        id="legacy",
        messages=[{"role": "user", "content": "hi"}],
        model="mock",
        max_tokens=10,
        temperature=0.0,
        seed=42,
    )
    assert s.bucket == "unspecified"


def test_grpc_chat_request_built_from_corpus_sample_carries_per_sample_fields() -> None:
    """When a corpus sample is provided to ``_send_chat_rpc``, the gRPC
    request payload's max_tokens, temperature, seed come from the sample
    — not from a hardcoded default."""
    from vllm_grpc_bench.m5_1_grpc_cohort import _send_chat_rpc  # noqa: F401  (import as guard)

    sample = RequestSample(
        id="corpus-x",
        messages=[
            {"role": "user", "content": "What is the capital of France?"},
        ],
        model="Qwen/Qwen3-0.6B",
        max_tokens=64,
        temperature=0.3,
        seed=123,
    )
    # Build the same request the cohort runner would, directly, to
    # inspect the protobuf encoding without an active channel.
    req = chat_pb2.ChatCompleteRequest(
        messages=[
            chat_pb2.ChatMessage(role=m["role"], content=m["content"]) for m in sample.messages
        ],
        model="mock-engine",
        max_tokens=sample.max_tokens,
        temperature=sample.temperature,
        seed=sample.seed,
    )
    assert req.messages[0].content == "What is the capital of France?"
    assert req.max_tokens == 64
    assert req.temperature == pytest.approx(0.3)
    assert req.seed == 123


@pytest.mark.asyncio
async def test_rest_cohort_chat_routes_through_corpus_when_provided() -> None:
    """End-to-end: ``run_rest_cohort(corpus=...)`` sends each iteration's
    corpus sample as the chat body. The captured wire bodies carry the
    corpus's messages + max_tokens + temperature verbatim.
    """
    corpus = _synthetic_corpus(n=3)
    captured_bodies: list[dict] = []

    async def _handler(request: httpx.Request) -> httpx.Response:
        if request.url.path == "/healthz":
            return httpx.Response(200, json={"ok": True})
        captured_bodies.append(json.loads(request.content))
        body = (
            b'data: {"choices":[{"delta":{"role":"assistant"}}]}\n\n'
            b'data: {"choices":[{"delta":{"content":"x"}}]}\n\n'
            b"data: [DONE]\n\n"
        )
        return httpx.Response(200, headers={"x-shim-overhead-ms": "0.1"}, content=body)

    transport = httpx.MockTransport(_handler)
    async with httpx.AsyncClient(
        transport=transport, base_url="http://test", http2=False
    ) as client:
        await run_rest_cohort(
            path="chat_stream",
            base_url="http://test",
            token=_TEST_TOKEN,
            concurrency=1,
            n=3,
            hidden_size=2048,
            rtt_probe_n=1,
            warmup_n=0,
            client=client,
            cell_id="rest-edge:test",
            corpus=corpus,
        )

    assert len(captured_bodies) == 3
    for i, body in enumerate(captured_bodies):
        sample = corpus[i % len(corpus)]
        assert body["messages"] == sample.messages, (
            f"REST iteration {i} did NOT send the corpus sample's messages — "
            f"got {body['messages']!r}, expected {sample.messages!r}"
        )
        assert body["max_tokens"] == sample.max_tokens
        assert body["temperature"] == sample.temperature
        # Synthetic-helper artifacts must NOT appear when corpus mode is on.
        assert "M5.2 chat probe" not in body["messages"][0]["content"]


@pytest.mark.asyncio
async def test_rest_cohort_cycles_corpus_by_iteration_index() -> None:
    """When n > len(corpus), the cohort runner cycles through the corpus
    by ``iteration % len(corpus)`` — every sample gets re-used the same
    number of times across REST and gRPC."""
    corpus = _synthetic_corpus(n=3)
    captured_prompts: list[str] = []

    async def _handler(request: httpx.Request) -> httpx.Response:
        if request.url.path == "/healthz":
            return httpx.Response(200, json={"ok": True})
        body = json.loads(request.content)
        captured_prompts.append(body["messages"][0]["content"])
        return httpx.Response(
            200,
            headers={"x-shim-overhead-ms": "0.1"},
            content=b'data: {"choices":[{"delta":{"content":"x"}}]}\n\ndata: [DONE]\n\n',
        )

    transport = httpx.MockTransport(_handler)
    async with httpx.AsyncClient(
        transport=transport, base_url="http://test", http2=False
    ) as client:
        await run_rest_cohort(
            path="chat_stream",
            base_url="http://test",
            token=_TEST_TOKEN,
            concurrency=1,
            n=7,
            hidden_size=2048,
            rtt_probe_n=1,
            warmup_n=0,
            client=client,
            cell_id="rest-test",
            corpus=corpus,
        )
    # 7 requests over a 3-sample corpus → samples 0,1,2,0,1,2,0.
    expected_indices = [0, 1, 2, 0, 1, 2, 0]
    for i, prompt in enumerate(captured_prompts):
        assert prompt == corpus[expected_indices[i]].messages[0]["content"]


@pytest.mark.asyncio
async def test_rest_cohort_corpus_none_falls_back_to_synthetic_prompts() -> None:
    """Back-compat: when ``corpus=None`` (default), the chat cohort uses
    the synthetic ``build_chat_prompt`` helper. Pre-corpus tests
    continue to pass."""
